    private void jBnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBnEditarActionPerformed
                if(JTMotorista.getSelectedRow() != -1){
            int motoristaSelecionado = (int)JTMotorista.getValueAt
                (JTMotorista.getSelectedRow(), 0);
            JFAtualizarMotorista av = new JFAtualizarMotorista(motoristaSelecionado);
            av.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um motorista!",
                    "Erro", JOptionPane.ERROR_MESSAGE);
        }
        readJTable();
                                           

    }//GEN-LAST:event_jBnEditarActionPerformed

    private void jBnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBnExcluirActionPerformed
         if(JTMotorista.getSelectedRow() != -1){
            int opcao = JOptionPane.showConfirmDialog(null,
                    "Deseja excluir o motorista selecionado?", "Exclus√£o",
                    JOptionPane.YES_NO_OPTION);
            if(opcao == 0){
                MotoristaDAO dao = new MotoristaDAO();
                Motorista m = new Motorista();
                m.setIdMotorista((int)JTMotorista.getValueAt(JTMotorista.getSelectedRow(), 0));
                dao.delete(m);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Selecione uma vaga!","Erro",
                    JOptionPane.ERROR_MESSAGE);
        }
        readJTable();
    }//GEN-LAST:event_jBnExcluirActionPerformed

